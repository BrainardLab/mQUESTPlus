% mQUESTPlus - demos
%
% Demonstration/test programs.  Two key demos are
%   1) qpQuestPlusPaperSimpleExampleDemo.  This runs several of the basic
%   demonstrations from the Watson (2017) QUEST+ paper, showing usage for
%   function qpRun.
%
%   2) qpQuestPlusCoreFunctionDemo. This illustrates how you can call the
%   core functions of mQUESTPlus directly, rather than letting qpRun
%   orchestrate your experiment.
%
% qpDataProcDemo                - Demonstrate/test the data processsing routines.
% qpPsiFunctionDemo             - Demonstrate/test the psychometric function routines.
% qpQuestPlusCoreFunctionDemo   - Show basic use of QUEST+ core functions,
%                                 called directly. This is the one to look
%                                 at if you don't want to hand off control
%                                 of your experiment to qpRun.
% qpQuestPlusCSFDemo            - Demonstrate/test QUEST+ at work on parametric
%                                 CSF estimation.
% qpQuestPlusCircularCatDemo    - Demonstrate/test QUEST+ at work on
%                                 categorization of a circular variable.
% qpQuestPlusPaperSimpleExamplesDemo - Demonstrate/test QUEST+ on some
%                                 simple example problems.  Illustrates
%                                 basic usage of function qpRun.
% qpRunAllDemos                 - Run through all of the QUEST+ demos.
% qpUtilityDemo                 - Demonstrate/test the qp utility routines.
